    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
//            String regexName = "^[A-Z][a-zA-Z]{1,29}$";
//
//            if (!(validateFields(regexName, txtFirstname))) {
//                JOptionPane.showMessageDialog(this, "Neispravno uneto ime.");
//                return;
//            }
//            if (!(validateFields(regexName, txtLastname))) {
//                JOptionPane.showMessageDialog(this, "Neispravno uneto prezime.");
//                return;
//            }
            Client c = new Client();
            if (!txtFirstname.getText().trim().isEmpty()) {
                c.setFirstname(txtFirstname.getText().trim());
            }
            if (!txtLastname.getText().trim().isEmpty()) {
                c.setLastname(txtLastname.getText().trim());
            }
            Service s = (Service) cmbServices.getSelectedItem();
            LocalDate dateFrom = null;
            LocalDate dateTo = null;
            if (dchDateFrom.getDate() != null) {
                dateFrom = dchDateFrom.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            }
            if (dchDateTo.getDate() != null) {
                dateTo = dchDateTo.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
            }
            
            
            ArrayList<Appointment> appointments = Controller.getInstance().getAllAppointment();
            searchedAppointments = new ArrayList<>();
            for (Appointment appointment : appointments) {
                boolean matches = true;

                if (s != null && !s.getName().equals(appointment.getService().getName())) {
                    matches = false;
                }
                if (c.getFirstname() != null && !c.getFirstname().toLowerCase().equals(appointment.getClient().getFirstname().toLowerCase())) {
                    matches = false;
                }
                if (c.getLastname() != null && !c.getLastname().toLowerCase().equals(appointment.getClient().getLastname().toLowerCase())) {
                    matches = false;
                }
                if (dateFrom != null && dateTo != null && (appointment.getDate().isBefore(dateFrom) || appointment.getDate().isAfter(dateTo))) {
                    matches = false;
                }

                if (matches) {
                    searchedAppointments.add(appointment);
                }
            }

            searched = true;
            rbSingle.setSelected(true);
            tblAppointments.setModel(new TableModelAllAppointment(searchedAppointments));

        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(DoneAppointments.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

