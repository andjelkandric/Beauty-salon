    private void fillCmbServices() {
        try {
            ArrayList<Service> services = Controller.getInstance().getAllServices();
            cmbServices.removeAllItems();

            for (Service service : services) {
                cmbServices.addItem(service);
            }

            cmbServices.setSelectedItem(null);
        } catch (Exception ex) {
            java.util.logging.Logger.getLogger(DoneAppointments.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }

    }

    private boolean validateFields(String regex, JTextField txtField) {
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(txtField.getText());
        return matcher.matches();
    }
