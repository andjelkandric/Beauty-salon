    private void btnAddSizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSizeActionPerformed
        try {
            if (dchDate.getDate() == null || txtHeight.getText().isEmpty() || txtChest.getText().isEmpty()
                    || txtHip.getText().isEmpty() || txtWaistSize.getText().isEmpty() || txtWeight.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Unesite sva polja.");
                return;
            }
            double height, chest, hip, waistSize, weight;
            
            try {
                height = Double.parseDouble(txtHeight.getText());
                chest = Double.parseDouble(txtChest.getText());
                hip = Double.parseDouble(txtHip.getText());
                waistSize = Double.parseDouble(txtWaistSize.getText());
                weight = Double.parseDouble(txtWeight.getText());
                
                if (height <= 0 || chest <= 0 || hip <= 0 || waistSize <= 0 || weight <= 0) {
                    JOptionPane.showMessageDialog(this, "Sve mere moraju biti pozitivni brojevi veÄi od nule.");
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Unesite ispravne brojeve za mere.");
                return;
            }
            
            long id=((TableModelSize)tblSize.getModel()).getId();
            Size size = new Size(id, client, dchDate.getDate(), waistSize, hip, chest, weight, height);
            
            Controller.getInstance().addSize(size);
            ((TableModelSize) tblSize.getModel()).addSize(size);
            txtChest.setText("");
            txtHeight.setText("");
            txtHip.setText("");
            txtWaistSize.setText("");
            txtWeight.setText("");
            dchDate.setDate(null);
        } catch (Exception ex) {
            Logger.getLogger(ClientDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddSizeActionPerformed

